export default function WeekTips({ birthDate }: { birthDate: string }) {
  const getWeekNumber = () => {
    const birth = new Date(birthDate);
    const now = new Date();
    const diff = now.getTime() - birth.getTime();
    const week = Math.floor(diff / (7 * 24 * 60 * 60 * 1000)) + 1;
    return week;
  };

  const currentWeek = getWeekNumber();
  const tip = weekTipsData[currentWeek] || "📌 해당 주차 정보가 아직 등록되지 않았습니다.";

  return (
    <div style={{ backgroundColor: "#e7f7ff", padding: "16px", borderRadius: "8px", marginTop: "20px" }}>

      <p style={{ lineHeight: "1.6" }}>{tip}</p>
    </div>
  );
}
